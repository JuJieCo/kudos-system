package com.jujie.task;

public class WorksAccessory {

	private Integer uuid;
	private Integer taskUuid;
	private String accessoryType;
	private String route;
	private String accessorySize;
	private String filename;
	public Integer getUuid() {
		return uuid;
	}
	public void setUuid(Integer uuid) {
		this.uuid = uuid;
	}
	public Integer getTaskUuid() {
		return taskUuid;
	}
	public void setTaskUuid(Integer taskUuid) {
		this.taskUuid = taskUuid;
	}
	public String getAccessoryType() {
		return accessoryType;
	}
	public void setAccessoryType(String accessoryType) {
		this.accessoryType = accessoryType;
	}
	public String getRoute() {
		return route;
	}
	public void setRoute(String route) {
		this.route = route;
	}
	public String getAccessorySize() {
		return accessorySize;
	}
	public void setAccessorySize(String accessorySize) {
		this.accessorySize = accessorySize;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	 
	 

	
}
